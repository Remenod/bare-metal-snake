#include <pic.h>

#include <ports.h>

void remap_pic()
{
    outb(0x20, 0x11);
    outb(0xA0, 0x11);

    outb(0x21, 0x20);
    outb(0xA1, 0x28);

    outb(0x21, 0x04);
    outb(0xA1, 0x02);

    outb(0x21, 0x01);
    outb(0xA1, 0x01);

    outb(0x21, 0xFC); // enable IRQ0 and IRQ1
    outb(0xA1, 0xFF); // disable all on slave
}
